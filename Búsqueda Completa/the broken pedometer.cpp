#include <bits/stdc++.h>
using namespace std;

bitset <15> numeros [100];
int mark[1<<15];

int main(){

    int casos,p,n; string a; char b;
    for(cin>>casos; casos--;){
    	cin>>p>>n;
        for(int i = 0; i<n; i++){
            a="";
            for (int j =0; j<p;j++){
               cin>>b;
               a+=b;
            }
            bitset<15> au (a);
            numeros[i] = au ;
        }
        bool val = 1; int minn = 1<<15 + 1;
        for(int i = 0; i< 1<<p; i++){
            memset(mark, 0, sizeof(mark));
            val =1;
            for(int j = 0; j<n; j++){
                unsigned long y = i & numeros[j].to_ulong();
                if(mark[y]) {val = 0; break;}
                mark[y] = 1;
            }
            if(val){
                    bitset <15> aux (i);
                    minn = min(minn, (int)aux.count());
            }
        }
        cout<<minn<<endl;
    }
    return 0;
}



